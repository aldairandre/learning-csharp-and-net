/*
  Create an Array
    Arrays are used to store multiple values in a single variable, instead of declaring separate variables for each value.

    To declare an array, define the variable type with square brackets:
*/

string[] cars = {"Volvo", "BMW", "Ford", "Mazda"};
int[] myNum = {10,20,30,40}; 
Console.WriteLine();
Console.WriteLine("Length of cars array: "+cars.Length);
Console.WriteLine("First element: "+cars[0]);
Console.WriteLine();
Console.WriteLine("Length of myNum array: "+cars.Length);
Console.WriteLine("First element: "+myNum[0]);
Console.WriteLine();